// Why Object-Oriented Programming?

// Before learning Object-Oriented Programming, it’s important to understand why it was needed.

// Earlier programming languages like C programming language mainly used procedural programming, where programs were written as a sequence of functions.

// As programs became larger and more complex, several problems appeared:

// Problems with Procedural Programming

// Poor readability as program size increased.

// Difficult maintenance when modifying or updating code.

// Higher chances of bugs.

// Data was not secure, because functions could access global data freely.

// Data and functions were separate, making programs harder to organize.

// To solve these issues, C++ introduced Object-Oriented Programming (OOP) concepts.

// OOP models programs using real-world objects, making software easier to design and maintain.

// Difference Between Procedural Programming and Object-Oriented Programming
// Procedural Oriented Programming	Object-Oriented Programming
// Program is divided into functions	Program is divided into objects
// Focus on functions and procedures	Focus on data and objects
// Data moves freely between functions	Data is protected inside objects
// Uses global data frequently	Uses encapsulation for data security
// Example: C	Example: C++, Java
// Basic Concepts in Object-Oriented Programming
// 1. Classes

// A class is a blueprint or template used to create objects.

// Example:

// class Car {
//    int speed;
//    void drive();
// }

// 2. Objects

// Objects are instances of a class and represent real-world entities.

// Example:

// Car myCar;

// 3. Data Abstraction

// Abstraction means showing only essential details and hiding unnecessary information.

// Example:
// When driving a car, you use the steering wheel and pedals, not the internal engine mechanisms.

// 4. Encapsulation

// Encapsulation means wrapping data and functions together in a single unit (class) and restricting direct access.

// This improves data security.

// 5. Inheritance

// Inheritance allows one class to reuse properties and methods of another class.

// Example:

// Vehicle → Car

// 6. Polymorphism

// Polymorphism means one function behaving in different ways depending on the situation.

// Example:

// print()


// can work differently for numbers, strings, etc.

// 7. Dynamic Binding

// Dynamic binding means the method to be executed is decided at runtime, not at compile time.

// 8. Message Passing

// Objects communicate with each other by sending messages (calling methods).

// Example:

// object.method()

// Benefits of Object-Oriented Programming

// Code Reusability
// Using inheritance and objects reduces repeated code.

// Data Security
// Encapsulation and data hiding protect sensitive information.

// Multiple Objects
// Many objects can exist independently without interfering with each other.

// Manageable Complexity
// Large software systems become easier to organize and maintain.